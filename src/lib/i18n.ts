// ============================================================================
// Translation Helper Hook
// ============================================================================
//
//  Usage:
//    const t = useTranslation();
//    <h1>{t("dashboard")}</h1>
//    <p>{t("welcomeBack")} {brandName}!</p>
//
//  Notes:
//    - Falls back to English, then to the raw key (so missing translations
//      never crash the UI).
//    - Supports {{placeholder}} interpolation via the optional `vars` arg.
//      Example:  t("trialDaysLeft", { days: 7 })  ->  "Trial mein 7 din bache hain"
// ============================================================================

export function useTranslation(): (
  key: string,
  fallback?: string,
  vars?: Record<string, string | number>
) => string {
  // Import useValtrioxStore dynamically to avoid circular dependency
  // eslint-disable-next-line @typescript-eslint/no-require-imports
  const { useValtrioxStore } = require("@/store/brandflow-store");
  const lang = useValtrioxStore((s: any) => s.language) || "en";

  return (
    key: string,
    fallback?: string,
    vars?: Record<string, string | number>
  ): string => {
    let str = translations[lang]?.[key] || translations.en[key] || fallback || key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(new RegExp(`\\{\\{${k}\\}\\}`, "g"), String(v));
      }
    }
    return str;
  };
}

/**
 * t() - standalone function (not a hook) that takes a language explicitly.
 * Useful in server components, utilities, and tests.
 */
export function t(
  lang: Language,
  key: string,
  fallback?: string,
  vars?: Record<string, string | number>
): string {
  let str = translations[lang]?.[key] || translations.en[key] || fallback || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{\\{${k}\\}\\}`, "g"), String(v));
    }
  }
  return str;
}

/**
 * getTranslationKeys() - returns all available translation keys.
 * Useful for audits, dev tools, and completeness checks.
 */
export function getTranslationKeys(): string[] {
  return Object.keys(translations.en);
}

/**
 * hasTranslation() - check if a key exists in a given language.
 */
export function hasTranslation(lang: Language, key: string): boolean {
  return Boolean(translations[lang]?.[key]);
}

export default translations;
